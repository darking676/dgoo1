package com.acorn.unicorn.bean;

public class CouponBean {
	private int c_num, c_discount, c_limit;
	private String c_bun, c_event, c_userid, c_gb;
	
	public int getC_num() {
		return c_num;
	}
	public void setC_num(int c_num) {
		this.c_num = c_num;
	}
	public int getC_discount() {
		return c_discount;
	}
	public void setC_discount(int c_discount) {
		this.c_discount = c_discount;
	}
	public int getC_limit() {
		return c_limit;
	}
	public void setC_limit(int c_limit) {
		this.c_limit = c_limit;
	}
	public String getC_bun() {
		return c_bun;
	}
	public void setC_bun(String c_bun) {
		this.c_bun = c_bun;
	}
	public String getC_event() {
		return c_event;
	}
	public void setC_event(String c_event) {
		this.c_event = c_event;
	}
	public String getC_userid() {
		return c_userid;
	}
	public void setC_userid(String c_userid) {
		this.c_userid = c_userid;
	}
	public String getC_gb() {
		return c_gb;
	}
	public void setC_gb(String c_gb) {
		this.c_gb = c_gb;
	}
	
	
}
